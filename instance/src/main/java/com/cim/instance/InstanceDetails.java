package com.cim.instance;

public class InstanceDetails {
	
	private String instanceid;
	private String instanceCreds;
	
	public String getInstanceid() {
		return instanceid;
	}
	public void setInstanceid(String instanceid) {
		this.instanceid = instanceid;
	}
	public String getInstanceCreds() {
		return instanceCreds;
	}
	public void setInstanceCreds(String instanceCreds) {
		this.instanceCreds = instanceCreds;
	}
	
	
}
